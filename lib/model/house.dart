class House {
  final String homeName;
  final String type;
  final String apartmentType;
  final String price;
  final String location;
  final String imageUrl;
  final int bedroom;
  final int bathrooms;

  House(
    this.homeName,
    this.type,
    this.apartmentType,
    this.price,
    this.location,
    this.imageUrl,
    this.bedroom,
    this.bathrooms,
  );
}
